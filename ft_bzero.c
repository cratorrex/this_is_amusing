//
// 0 0 0 0 0 0 87 111 114 108 100 33
// strings.h >> size_t
//

#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	while (n > 0)
	{
		*(char *)s = 0;
		s++;
		n--;
	}
}
