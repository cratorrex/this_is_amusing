//
// Too much, Too little.
//

#include "libft.h"

int	ft_tolower(int c)
{
	int	r;

	r = 0;
	if (c >= 65 && c <= 90)
	{
		r = 32 + c;
		return (r);
	}
	else
	{
		return (c);
	}
}
